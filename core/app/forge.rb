# website on project download; defines `Forge::API_KEY` and `Forge::API_URL`.
# Gitignored — never commit it.
if Forge::Fs.exists?("api_key.rb")
  require "api_key"
else
  module Forge
    class << self
      def api_key
        raise "Forge API key not configured. Download a project from https://forge.game to get your API key."
      end

      def api_url
        @config&.api_url || "https://forge.game/api"
      end
    end
  end
end
