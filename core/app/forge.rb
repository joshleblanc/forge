module Forge
  class Config
    attr_accessor :game_class, :ui_theme, :api_url, :api_key

    def initialize
      @api_url = "https://forge.game/api"
      @ui_theme = nil
      @game_class = nil
    end
  end

  # Asset maps loaded per package (populated by packages.rb)
  class << self
    attr_accessor :config, :asset_maps

    def configure(&blk)
      @config ||= Config.new
      blk.call(@config) if blk
      @config
    end

    # Per-frame entry point. Delegates to the configured game class if one is
    # set (so the orchestrator gets pre_update / update / post_update / render),
    # otherwise falls back to a plain Process update tick for empty projects.
    def tick(args)
      game_class = @config&.game_class
      if game_class
        instance = game_class.respond_to?(:s) ? game_class.s : (@game_instance ||= game_class.new)
        instance.args = args if instance.respond_to?(:args=)
        if instance.respond_to?(:tick)
          instance.tick
        else
          Process.update_all(1)
          Scheduler.tick
        end
      else
        Process.update_all(1)
        Scheduler.tick
      end

      # Advance any pending async HTTP requests every frame.
      Http.tick
    end

    # Resolve an asset path within a package.
    # @param package_name [String] package name
    # @param logical_path [String] logical path within package (e.g., "sprites/player.png")
    # @return [String, nil] resolved absolute path or nil if not found
    #
    # Usage:
    #   Forge.asset_path("ldtk_parser", "data/levels/demo.ldtk")
    #   # => "/path/to/project/packages/ldtk_parser/assets/data/levels/demo.ldtk"
    def asset_path(package_name, logical_path)
      return logical_path if logical_path.start_with?("/")
      return logical_path if logical_path.start_with?("packages/")

      maps = @asset_maps || {}
      pkg_map = maps[package_name] || {}

      # Try exact match first
      pkg_map[logical_path] ||
        pkg_map["#{logical_path}.png"] ||
        pkg_map[logical_path.sub(".png", "")]
    end

    # Get the directory path for a package.
    # @param package_name [String]
    # @return [String] path to package directory
    def package_dir(package_name)
      File.join("packages", package_name)
    end

    # Get the assets directory for a package.
    # @param package_name [String]
    # @return [String] path to package assets directory
    def package_assets_dir(package_name)
      File.join("packages", package_name, "assets")
    end

    # List all asset paths for a package.
    # @param package_name [String]
    # @return [Hash] logical_name => absolute_path mapping
    def package_assets(package_name)
      (@asset_maps || {})[package_name] || {}
    end
  end
end

